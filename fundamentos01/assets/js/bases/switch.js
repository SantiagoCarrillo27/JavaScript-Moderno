

const dia = 0 ; // Dias para Javascript el 0 es Domingo y así sucesivamente

switch (dia) {

    case 0:
        console.log('Domingo');
        break
    case 1:
        console.log('Lunes');
        break
    case 2:
        console.log('Martes');
        break
    case 3:
        console.log('Miercoles');
        break
    case 4:
        console.log('Jueves');
        break
    case 5:
        console.log('Viernes');
        break
    case 6:
        console.log('Sàbado');
        break

    default: console.log('No hay dìas disponibles');
}

